// - 플레이어에게 부여된 역할은 기본적으로 청소부(?)임.
// - "청소부로서 당신은 고물을 수집할 수 있지만, 그 처리는 본인이 해야 합니다. 저기 누군가(브로커or수상한고물상)이 부르네요. --- 성공하고싶냐"
// - 한 100개쯤 되는 선택지에서 선택해서 줍는데 그 폐지의 가치가 각각 다른거임.
