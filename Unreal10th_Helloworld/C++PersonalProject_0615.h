// - 전투시 가지고 있는 무기 중 선택할 수 있고, 던전 클리어시 확정보상으로 랜덤 폐기물이 나옴. (가치는 보통보다 높게)
// - 모든 보상은 기본적으로 금액이지만, 모은 금액으로 게임에 도전할 수 있음. (지참금이 좀 커야 할 듯)
// - 게임을 시작하면 승패 여부와 상관없이 비용이 차감되고, 승리시 보상 : 레벨업. 다음 게임 해금.
// - 레벨이 올라갈수록 (수집기, 분해기가 업그레이드) 폐기물의 기본 가치가 20%씩 올라감. (판매/분해 모두)
// - 10레벨 달성 시, 엔딩 시작 (모은 돈으로 성공하려고 하지만 아무튼 방해)
// - 고물상이 빌런이었음. 그동안 받아간 폐기물과 분해물로 폐기물 지옥을 만들어 데려옴.
// - 총 두 번의 라스트게임. 1. 폐기물 던전 / 2. 고물상과의 단판게임(Indian Game?)
// - 던전은 실패해도 다시 도전할 수 있지만 게임은 패배 시 그대로 돈 다 뺏기고 배드엔딩.
// - 게임까지 모두 통과 시, 고물상은 사라지고 돈도 그대로에 영웅으로 추앙받으며 해피엔딩.
